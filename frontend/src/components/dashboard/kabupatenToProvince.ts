const _reverseCache: Record<string, string[]> = {};
export function getKabupatenForProvince(provinceCode: string): string[] {
  if (!_reverseCache[provinceCode]) {
    _reverseCache[provinceCode] = Object.entries(KABUPATEN_TO_PROVINCE)
      .filter(([, code]) => code === provinceCode)
      .map(([id]) => id);
  }
  return _reverseCache[provinceCode];
}
